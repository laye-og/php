								<p class="ini">Comment répondre, écrire ou poster un message sur le forum ?</p> 


Pour se présenter aux autres membres du forum, écrire ou répondre	,  il n'y a qu'une seule petite contrainte, vous devez d'abord vous enregistrer. Ensuite, voici comment vous devez procéder :<br>	

<u>1ère étape</u><br>	
Tout d'abord, ayez le reflexe de vérifier si vous êtes connecté.	
Si ce n'est pas le cas, connectez vous en bas de la page d'accueil< 	en tapant dans la première case votre nom d'utilisateur	, dans la seconde votre mot de passe et pour finir cliquer sur "connexion"<br>	

<u>2ème étape</u><br>	
Choisissez le forum où vous allez poster votre message.
Par exemple pour "Présentations", cliquer sur le lien-titre "Présentations".
Sur la page qui s'affiche et pour écrire un nouveau message, 	cliquer sur le bouton "nouveau"<br>

<u>3ème étape</u><br>	
Sur cette page, vous allez pouvoir rédiger votre message.	
Commencer par lui donner un titre dans l'espace "sujet" puis taper	 votre texte dans le cadre du message.
Dès à présent, vous pouvez visualiser votre message en cliquant sur le bouton "prévisualisation".	
Si vous êtes satisfait de votre message, postez-le en cliquant sur	 le bouton "envoyer"<br>	

<u>4ème étape</u><br>	
Pour répondre à un message, reprenons l'exemple "Présentations".
Cliquer sur le lien-titre "Présentations". Sur la page qui	 s'affiche, vous cliquez sur le sujet voulu puis sur "répondre" et	vous procédez de la même manière que pour "nouveau".	</p>

							

